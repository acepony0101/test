package model;

import java.sql.Date;

/**
 *
 * @author Aquarius
 */
public class Feedback {

    private int id;
    private String customerName, content, type;
    private Date date;

    public Feedback(int id, String customerName, String content, String type, Date date) {
        this.id = id;
        this.customerName = customerName;
        this.content = content;
        this.type = type;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
    

}
