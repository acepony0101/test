    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        if (getPhone(request, response) == null) {
            response.sendRedirect(request.getContextPath() + "/login");
        } else {
            request.getRequestDispatcher("feedback.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String type = request.getParameter("type");
        String content = request.getParameter("content");
        String phone = getPhone(request, response);
        LocalDateTime currentDateTime = LocalDateTime.now();
        DateTimeFormatter formatterDate = DateTimeFormatter.ofPattern("yyyyMMdd");
        String date = currentDateTime.format(formatterDate);
        CategoryDAO c = new CategoryDAO();
        c.addFeedback(type, content, date, phone);
        request.getRequestDispatcher("finishFeedback.jsp").forward(request, response);
    }

    private String getPhone(HttpServletRequest request, HttpServletResponse response) {
        Cookie cookie[] = request.getCookies();
        for (Cookie o : cookie) {
            if (o.getName().equals("phone")) {
                return o.getValue();
            }
        }
        return null;
    }

